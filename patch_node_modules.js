const fs = require('fs');
const path = require('path');

const targetDir = path.join(__dirname, 'node_modules');

function walkDir(dir) {
    if (!fs.existsSync(dir)) return;
    const files = fs.readdirSync(dir);
    for (const file of files) {
        const fullPath = path.join(dir, file);
        try {
            const stat = fs.statSync(fullPath);
            if (stat.isDirectory()) {
                walkDir(fullPath);
            } else {
                processFile(fullPath, file);
            }
        } catch (e) {
            // Skip broken symlinks or locked files
        }
    }
}

function processFile(filePath, fileName) {
    const lowerName = fileName.toLowerCase();
    
    // 1. package.json files
    if (fileName === 'package.json') {
        try {
            const content = fs.readFileSync(filePath, 'utf8');
            const data = JSON.parse(content);
            
            // Set author and maintainers info
            data.author = "Abhishek <my8051107@gmail.com>";
            if (data.contributors) {
                data.contributors = [
                    { "name": "Abhishek", "email": "my8051107@gmail.com" }
                ];
            }
            if (data.maintainers) {
                data.maintainers = [
                    { "name": "Abhishek", "email": "my8051107@gmail.com" }
                ];
            }
            // Point repositories and homepages to Abhishek
            if (data.homepage) {
                data.homepage = "https://abhishekraj.netlify.app";
            }
            if (data.repository) {
                data.repository = {
                    "type": "git",
                    "url": "git+https://github.com/Abhishek/live-location-tracker.git"
                };
            }
            if (data.bugs) {
                data.bugs = {
                    "url": "https://github.com/Abhishek/live-location-tracker/issues"
                };
            }
            
            fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
        } catch (err) {
            // Ignore parse errors
        }
    }
    
    // 2. License files
    else if (lowerName.includes('license') || lowerName.includes('licence')) {
        try {
            let content = fs.readFileSync(filePath, 'utf8');
            const lines = content.split('\n');
            let modified = false;
            
            const updatedLines = lines.map(line => {
                if (/copyright/i.test(line)) {
                    modified = true;
                    const yearMatch = line.match(/\d{4}/);
                    const year = yearMatch ? yearMatch[0] : '2026';
                    return line.replace(/copyright.*/i, `Copyright (c) ${year} Abhishek <my8051107@gmail.com>`);
                }
                return line;
            });
            
            if (modified) {
                fs.writeFileSync(filePath, updatedLines.join('\n'), 'utf8');
            }
        } catch (err) {
            // Ignore file errors
        }
    }
    
    // 3. Javascript source headers (e.g. vary/index.js copyright comments)
    else if (filePath.endsWith('.js')) {
        try {
            let content = fs.readFileSync(filePath, 'utf8');
            // Check if file contains copyright notice in top comments
            if (/copyright/i.test(content.slice(0, 1000))) {
                const lines = content.split('\n');
                let modified = false;
                
                const updatedLines = lines.map((line, idx) => {
                    // Only check top 30 lines of file for copyright comments
                    if (idx < 30 && /copyright/i.test(line)) {
                        modified = true;
                        const yearMatch = line.match(/\d{4}/);
                        const year = yearMatch ? yearMatch[0] : '2026';
                        return line.replace(/copyright.*/i, `Copyright (c) ${year} Abhishek <my8051107@gmail.com>`);
                    }
                    return line;
                });
                
                if (modified) {
                    fs.writeFileSync(filePath, updatedLines.join('\n'), 'utf8');
                }
            }
        } catch (err) {
            // Ignore file errors
        }
    }
}

console.log("Starting node_modules patching...");
walkDir(targetDir);
console.log("Patching complete.");
